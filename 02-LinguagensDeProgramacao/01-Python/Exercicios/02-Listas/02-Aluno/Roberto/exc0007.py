print('[-- Desenvolva um programa que leia as duas notas de um aluno, calcula e mostre a sua média. --]\n')
aluno = input('Digite o nome do Aluno: ')
nota1 = int(input('Digite a primeira nota do aluno:'))
nota2 = int(input('Digite a segunda nota do aluno:'))
media = (nota1+nota2)/2
print('A média do aluno {} foi {}' .format(aluno, media))



